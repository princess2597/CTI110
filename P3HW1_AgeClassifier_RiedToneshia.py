YourAge = int( input("Please enter your age:"))
if YourAge <= 1:
    print("You are an infant")
elif YourAge < 13:
    print ("You are a child")
elif YourAge < 20:
    print ("You are a teenager")
elif YourAge >= 20:
    print ("You are an adult")
        
