import turtle
wn = turtle.Screen()
wn.bgcolor("pink")
wn.title("Tess & Alex")

tess = turtle.Turtle()
tess.color("purple")
tess.pensize(5)

alex = turtle.Turtle()
alex.pensize(5)

tess.forward(80)
tess.left(120)
tess.forward(80)
tess.left(120)
tess.forward(80)
tess.left(120)



alex.forward(50)
alex.left(90)
alex.forward(50)
alex.left(90)
alex.forward(50)
alex.left(90)
alex.forward(50)
          
